def qq_face_image_description(face_id: str | None) -> str | None:
    """Return the final semantic image description for one QQ face id.

    Args:
        face_id: Platform-provided QQ face id from a CQ marker or segment.

    Returns:
        The curated prompt-facing description, or ``None`` when the id is not
        usable or not present in the accepted static catalog.
    """

    if face_id is None:
        return_value = None
        return return_value

    normalized_face_id = str(face_id).strip()
    if not normalized_face_id:
        return_value = None
        return return_value
    if not normalized_face_id.isascii() or not normalized_face_id.isdecimal():
        return_value = None
        return return_value

    description = QQ_FACE_IMAGE_DESCRIPTIONS.get(normalized_face_id)
    if description is None or not description.strip():
        return_value = None
        return return_value

    return_value = description
    return return_value
